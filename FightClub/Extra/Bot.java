package FightClub.Extra;

public class Bot {
    double energy, x,y;

    public Bot(double energy, double x, double y) {
        this.energy = energy;
        this.x = x;
        this.y = y;
    }

    public double getEnergy() {
        return energy;
    }

    public void setEnergy(double energy) {
        this.energy = energy;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
